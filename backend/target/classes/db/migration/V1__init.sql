CREATE TABLE users (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email         VARCHAR(255) NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    name          VARCHAR(255),
    created_at    TIMESTAMPTZ  NOT NULL DEFAULT now(),
    CONSTRAINT uq_users_email UNIQUE (email)
);

CREATE TABLE daily_logs (
    id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id            BIGINT      NOT NULL,
    log_date           DATE        NOT NULL,
    weight             NUMERIC(5, 2),
    sleeping_hours     NUMERIC(4, 2),
    steps              INTEGER,
    training_completed BOOLEAN,
    protein_grams      INTEGER,
    calories           INTEGER,
    water_liters       NUMERIC(4, 2),
    overeating         BOOLEAN,
    trigger_type       VARCHAR(100),
    resisted_trigger   BOOLEAN,
    energy_level       SMALLINT,
    stress_level       SMALLINT,
    mood_level         SMALLINT,
    notes              TEXT,
    CONSTRAINT fk_daily_logs_user FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE,
    CONSTRAINT uq_daily_logs_user_date UNIQUE (user_id, log_date)
);

CREATE INDEX idx_daily_logs_user_id ON daily_logs (user_id);

CREATE TABLE weekly_reviews (
    id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id                 BIGINT      NOT NULL,
    week_start_date         DATE        NOT NULL,
    week_end_date           DATE        NOT NULL,
    avg_weight              NUMERIC(5, 2),
    weight_change           NUMERIC(5, 2),
    avg_sleep_hours         NUMERIC(4, 2),
    avg_steps               INTEGER,
    training_cnt            SMALLINT,
    days_without_overeating SMALLINT,
    avg_energy_level        NUMERIC(3, 1),
    avg_stress_level        NUMERIC(3, 1),
    avg_mood_level          NUMERIC(3, 1),
    avg_calories            INTEGER,
    what_went_well          TEXT,
    biggest_problem         TEXT,
    next_week_focus         TEXT,
    created_at              TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at              TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_weekly_reviews_user FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE,
    CONSTRAINT uq_weekly_reviews_user_week UNIQUE (user_id, week_start_date)
);

CREATE INDEX idx_weekly_reviews_user_id ON weekly_reviews (user_id);

CREATE TABLE monthly_reviews (
    id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id                 BIGINT      NOT NULL,
    period_start_date       DATE        NOT NULL,
    period_end_date         DATE        NOT NULL,
    weight                  NUMERIC(5, 2),
    waist                   NUMERIC(5, 2),
