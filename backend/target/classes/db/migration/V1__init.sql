    neck                    NUMERIC(5, 2),
    biceps_left             NUMERIC(5, 2),
    biceps_right            NUMERIC(5, 2),
    thigh_left              NUMERIC(5, 2),
    thigh_right             NUMERIC(5, 2),
    confidence_level        SMALLINT,
    self_satisfaction_level SMALLINT,
    food_control_level      SMALLINT,
    notes                   TEXT,
    created_at              TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at              TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_monthly_reviews_user FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE,
    CONSTRAINT uq_monthly_reviews_user_period UNIQUE (user_id, period_start_date)
